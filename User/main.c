static void LED1_Task(void* parameter)
{		vTaskDelay(500);
    while (1)
    {
    	if(USART_RX_STA&0x8000)
			{			   		
				if(USART_RX_BUF[0] == 0x00)
				{
						relay_off();
						vTaskDelay(500);
						relay_on();
				}
				USART_RX_STA=0;
			}		
    }
}

